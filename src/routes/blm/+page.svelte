        <h1 align="center">Black Lives Matter</h1>

        <p style= "text-align:center;">
            <b>For fans of Cal Animage Alpha, a statement from our leadership regarding the resurgent Black Lives Matter movement.</b>
        <p style= "text-align:center;">
            Cal Animage Alpha would like to express its support for the Black community and Black Lives Matter during this time.
            We realize how important it is to use our platform to speak about this ever present circumstance and to show our support for the Black community,
            and we sincerely apologize for our failure to take action sooner.
        <p style= "text-align:center;">
            <b>UC Berkeley has become notorious for its failures to uphold and uplift its small Black minority.</b>
            Its founders, still ever-present by name across campus and the city,
            published xenophobic discourse and participated in chattel slavery.
            Our Black Student Union pressured UC Berkeley administration in 2015 to follow the steps of other schools and make reparations by changing the names of certain buildings
            (see <a href="https://www.dailycal.org/2015/07/09/uc-berkeley-students-call-for-renaming-of-campus-buildings-tied-to-confederacy/"><span>here</span></a>).
        <p style= "text-align:center;">
            <b>It is 2020, and no progress has been made.</b>
        <p style= "text-align:center;">
            Not only is Cal Animage Alpha working under the framework of a university that has long failed to support the Black community,
            but we are also representative of a more general population that is rife with anti-blackness.
            <b>There are many ways in which fans of anime have disregarded or deprecated its Black community.</b>
            Black cosplayers face an unmatched experience; targeted for portraying characters of a different skin color,
            attackers mask anti-blackness as “criticizing inaccuracy”. Cal Animage Alpha condemns this discrimination.
            This, however, is only one example of the many complex ways anti-blackness pervades the anime community.
        <p style= "text-align:center;">
            The leadership at Cal Animage Alpha has also recognized its own failure to address discrimination during times of crisis in the past.
            <b>On March 8, 2019, two Black students were arrested and brutalized by UCPD on our campus.</b>
            During that incident and many others, we had unfortunately chosen to remain silent.
        <p style= "text-align:center;">
            For these reasons, we must explicitly demonstrate our support for our Black community and Black Lives Matter.
        <br>
            <b>Cal Animage Alpha has decided to donate club funds to Black organizations specifically in the Bay Area.</b>
            We have chosen to donate an equally divided amount of one-thousand dollars raised to our own Black Student Union,
            the Anti Police-Terror Project, the Transgender Gender-Variant & Intersex Justice Project, the Black Bay Area (The BBA, LLC),
                and the Black Owned Business Relief Fund.
        <p style= "text-align:center;">
            <b>We would like to use our platform to ask those who are capable to donate to the same.</b>
        <p style= "text-align:center;">
            <a href="https://twitter.com/CalBSU"><span>CalBSU Twitter</span></a> Venmo: @calbsu
        <p style= "text-align:center;">
            <a href="https://www.antipoliceterrorproject.org/donate"><span>Anti Police Terror Project</span></a>
        <p style= "text-align:center;">
            <a href="http://www.tgijp.org/donate.html"><span>Transgender Gender-variant and Intersex Justice Project (TGIJP)</span></a>
        <p style= "text-align:center;">
            <a href="https://linktr.ee/theblackbayarea"><span>The Black Bay Area</span></a>
        <p style= "text-align:center;">
            <a href="https://www.gofundme.com/f/bay-area-black-owned-business-relief-fund"><span>Black Owned Business Relief Fund Gofundme</span></a>
        <p style= "text-align:center;">
            <b>If you are unable to donate, there are many other ways to show your support to our Black community and Black Lives Matter:</b>
        <p style= "text-align:center;">
            <b>Donate by watching YouTube videos whose ad revenue goes towards bail funds, Black associations,
            and other causes relevant to BLM.</b> For example: <a href="https://www.youtube.com/watch?v=bCgLa25fDHM"><span>https://www.youtube.com/watch?v=bCgLa25fDHM</span></a>
        <p style= "text-align:center;">
            <b>Support Black-owned businesses and Black creators.</b> Choose goods and services from Black-owned businesses whenever possible.
        <p style= "text-align:center;">
            <b><a href="https://blacklivesmatters.carrd.co/#petitions"><span>Sign petitions for action.</span></a></b>
        <p style= "text-align:center;">
            <b>Be wary of performative trends on social media, such as posting a black square to “show support” for the movement.</b>
            Many of them end up clogging #BlackLivesMatter and related tags, drowning out Black voices.
        <p style= "text-align:center;">
            <b>For more information, consider this document created specifically to outline various ways to support the Black community in the Bay Area.</b>
            <a href="https://docs.google.com/document/d/1Vv3zHARNDyI4xhh1z8w1mwdLgPAZ5LDVpyKlBEK_7mA/edit"><span>https://docs.google.com/document/d/1Vv3zHARNDyI4xhh1z8w1mwdLgPAZ5LDVpyKlBEK_7mA/edit</span></a>
        <p style= "text-align:center;">
            The leadership at Cal Animage Alpha would like to move forward from this action to being more aware of its influence as an established organization at UC Berkeley.
            Though we are a hobby-driven club united by a form of media enjoyed by many, it is especially important to use our reach in times of need.
            We would like to remind our community that your voice is important, and we will try our best to support you the best we can.
        <p style= "text-align:center;">
            Stay safe everyone, and see you soon!
        <p style= "text-align:center;">
            Cal Animage Alpha
